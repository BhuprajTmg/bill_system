
public class Users {
	private String txt_fname;
	private String txt_lname;
	private String txt_password;
	private String txt_pin;
	
	
	public String getTxt_fname() {
		return txt_fname;
	}
	public void setTxt_fname(String txt_fname) {
		this.txt_fname = txt_fname;
	}
	public String getTxt_lname() {
		return txt_lname;
	}
	public void setTxt_lname(String txt_lname) {
		this.txt_lname = txt_lname;
	}
	public String getTxt_password() {
		return txt_password;
	}
	public void setTxt_password(String txt_password) {
		this.txt_password = txt_password;
	}
	public String getTxt_pin() {
		return txt_pin;
	}
	public void setTxt_pin(String txt_pin) {
		this.txt_pin = txt_pin;
	}
	
	
	
	

}
